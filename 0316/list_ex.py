# 리스트 num 생성 및 출력
num = [10, 20, 30, 40, 50]
print(num)

# 리스트 데이터 출력
print("첫 번째 데이터: %d" % num[0])
print("두 번째 데이터: %d" % num[1])
print("세 번째 데이터: %d" % num[2])
print("네 번째 데이터: %d" % num[3])
print("다섯 번째 데이터: %d" % num[4])

# 리스트 데이터 뎃셈 연산
result = num[2] + num[4]
print("덧셈 연산: %d" % result)

# 리스트 데이터 추가 및 출력
num.append(60)
print(num)